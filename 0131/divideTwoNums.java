public class divideTwoNums {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        int result = a / b;
        System.out.println(result);

        double result2 = (double) a / b;
        System.out.println(result2);

        double c = 10;
        double d = 3;
        double result3 = c / d;
        System.out.println(result3);
    }
}
